#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int		i;
	int		count;

	i = 0;
	count = 0;
	while (s[count] != '\0')
		count++;
	while (count > i)
	{
		(*f)(s);
		i++;
		s++;
	}
}
